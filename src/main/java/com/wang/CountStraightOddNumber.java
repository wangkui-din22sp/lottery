package com.wang;

public class CountStraightOddNumber {
   
        byte countStraightOddNumber = 1;
        long startingEntryNumber = 0;
        long endingEntryNumber = 0;

        public CountStraightOddNumber(byte countStraightOddNumber, long startingEntryNumber, long endingEntryNumber) {
            this.countStraightOddNumber = countStraightOddNumber;
            this.startingEntryNumber = startingEntryNumber;
            this.endingEntryNumber = endingEntryNumber;
        }

        public byte getCountStraightOddNumber() {
            return countStraightOddNumber;
        }

        public long getStartingEntryNumber() {
            return startingEntryNumber;
        }

        public long getEndingEntryNumber() {
            return endingEntryNumber;
        }
    
    
    

}
